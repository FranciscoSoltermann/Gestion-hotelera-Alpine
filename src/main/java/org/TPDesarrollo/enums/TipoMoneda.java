package org.TPDesarrollo.enums;

/**
 * Define los tipos de monedas aceptadas para transacciones de pago y cotización en el sistema.
 */
public enum TipoMoneda {
    /**
     * Moneda de curso legal en Argentina (ARS).
     */
    PESOS,

    /**
     * Dólar estadounidense (USD).
     */
    DOLARES,

    /**
     * Moneda de curso legal en la Eurozona (EUR).
     */
    EUROS,

    /**
     * Moneda de curso legal en Brasil (BRL).
     */
    REALES,

    /**
     * Moneda de curso legal en Uruguay (UYU).
     */
    PESOS_UY
}